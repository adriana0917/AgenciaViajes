    private void limpiar() {
        txtNombre.setText("");       
        perSeleccion = 0;
    }
    
    public void cargarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) tablaAerolinea.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        Object Fila[] = new Object[tablaAerolinea.getColumnCount()];
        Aerolinea aero = new Aerolinea();
        for (Aerolinea c : aero.mostrarAerolinea()) {
            Fila[0] = c.getCodAerolinea();
            Fila[1] = c.getNombre();           
            modelo.addRow(Fila);
        }
        tablaAerolinea.setModel(modelo);
    }
    
    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        Aerolinea aerolinea = new Aerolinea();
        int dialogResult = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar esta aerolinea", "Agencia De Viajes", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            aerolinea.eliminarAerolinea(perSeleccion);
            limpiar();
            cargarDatos();
        } else {
            limpiar();
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

    private void tablaAerolineaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaAerolineaMouseClicked
        txtNombre.setText(tablaAerolinea.getValueAt(tablaAerolinea.getSelectedRow(), 1).toString());        
        perSeleccion = Integer.parseInt(tablaAerolinea.getValueAt(tablaAerolinea.getSelectedRow(), 0).toString());  
    }//GEN-LAST:event_tablaAerolineaMouseClicked


