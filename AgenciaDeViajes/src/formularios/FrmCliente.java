    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        Cliente c = new Cliente();
        int dialogResult = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar este Cliente", "Agencia de Viajes", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            c.eliminarCliente(perSeleccion);
            limpiar();
            cargarDatos();
        } else {
            limpiar();
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

    private void tablaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClienteMouseClicked
        txtNombre.setText(tablaCliente.getValueAt(tablaCliente.getSelectedRow(), 1).toString());
        txtDireccion.setText(tablaCliente.getValueAt(tablaCliente.getSelectedRow(), 2).toString());
        txtTelefono.setText(tablaCliente.getValueAt(tablaCliente.getSelectedRow(), 3).toString());
        txtCorreo.setText(tablaCliente.getValueAt(tablaCliente.getSelectedRow(), 4).toString());
        perSeleccion = Integer.parseInt(tablaCliente.getValueAt(tablaCliente.getSelectedRow(), 0).toString());
    }//GEN-LAST:event_tablaClienteMouseClicked

