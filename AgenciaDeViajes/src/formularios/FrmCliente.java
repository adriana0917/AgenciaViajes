    private void limpiar() {
        txtNombre.setText("");       
        txtDireccion.setText(""); 
        txtTelefono.setText(""); 
        txtCorreo.setText(""); 
        perSeleccion = 0;
    }
    
    public void cargarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) tablaCliente.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        Object Fila[] = new Object[tablaCliente.getColumnCount()];
        Cliente cl = new Cliente();
        for (Cliente c : cl.mostrarCliente()) {
            Fila[0] = c.getCodCliente();
            Fila[1] = c.getNombre(); 
            Fila[2] = c.getDireccion(); 
            Fila[3] = c.getTelefono(); 
            Fila[4] = c.getCorreo(); 
            modelo.addRow(Fila);
        }
        tablaCliente.setModel(modelo);
    }            

